let nome = 'Dudu'

console.log(nome.charAt(0)) //pegar uma letra 

console.log(nome.charCodeAt(3)) //valor na tabela ascii

console.log(nome.indexOf('D')) //retorna a posição da letra

console.log(nome.substring(1)) //retora a string do indice indicado até o final

console.log(nome.substring(0, 3))

console.log(nome.replace('D', 34)) //troca o caractere dito por outro